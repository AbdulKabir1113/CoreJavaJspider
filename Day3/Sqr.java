class Sqr {
    public static void perameterSq(int a){
        
        int per=4*a;
        System.out.println(per);
    }

    public static void areaSq(int a){
        
        int area=a*a;
        System.out.println(area);
    }
    public static void main(String[] args) {
        perameterSq(4);
        areaSq(6);
    }
}
