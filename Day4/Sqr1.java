class Sqr1 {
    
    public static int perameterSq(int a){
        
        int per=4*a;
        return(per);
    }

    public static int areaSq(int a){
        
        int area=a*a;
        return(area);
    }
    public static void main(String[] args) {
        System.out.println(perameterSq(5));
        System.out.println(areaSq(7));
    }
}

