package nonPrimitiveTC;

public class Cab {

	    private String pl; 
	    private String dl; 

	    public Cab(String pl, String dl) {
	        this.pl = pl;
	        this.dl = dl;
	    }

	    public String getPl() {
	        return pl;
	    }

	    public String getDl() {
	        return dl;
	    }	
}
